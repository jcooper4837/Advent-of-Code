import java.util.*;

public class P03_2
{
    public static void main(String[] args)
    {
        Scanner s = new Scanner(System.in);
        ArrayList<String> arr = new ArrayList();
        while (true)
        {
            arr.add(s.nextLine());
            if (arr.get(arr.size() - 1).equals("0"))
            {
                arr.remove(arr.size() - 1);
                break;
            }
        }
        int list[] = new int[10], count = 0, length = arr.get(0).length();
        list[0] = 1;
        list[1] = 3;
        list[2] = 5;
        list[3] = 7;
        list[4] = 1;
        for (int i = 5; i < list.length; i++)
        {
            list[i] = 0;
        }
        for (int i = 1; i < arr.size(); i++)
        {
            if (arr.get(i).charAt(list[0]) == '#')
            {
                list[5]++;
            }
            if (arr.get(i).charAt(list[1]) == '#')
            {
                list[6]++;
            }
            if (arr.get(i).charAt(list[2]) == '#')
            {
                list[7]++;
            }
            if (arr.get(i).charAt(list[3]) == '#')
            {
                list[8]++;
            }
            if (i % 2 == 0 && arr.get(i).charAt(list[4]) == '#')
            {
                list[9]++;
            }
            list[0]++;
            list[1] += 3;
            list[2] += 5;
            list[3] += 7;
            if (i % 2 == 0)
            {
                list[4]++;
            }
            if (list[0] >= length)
            {
                list[0] -= length;
            }
            if (list[1] >= length)
            {
                list[1] -= length;
            }
            if (list[2] >= length)
            {
                list[2] -= length;
            }
            if (list[3] >= length)
            {
                list[3] -= length;
            }
            if (list[4] >= length)
            {
                list[4] -= length;
            }
        }
        System.out.println(list[5]+" "+list[6]+" "+list[7]+" "+list[8]+" "+list[9]);
        //System.out.println(list[5]*list[6]*list[7]*list[8]*list[9]);
    }
}

